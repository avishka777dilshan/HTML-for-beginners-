function onMouse(){
    document.getElementById("color").style.color="red";
}
function outMouse(){
    document.getElementById("color").style.color="black";
}

function bigImg(x){
    x.style.height = "800px";
    x.style.width = "800px";
}
function normalImg(x){
    x.style.height = "640px";
    x.style.width = "640px";
}